    public void cargarCombo() {
        DefaultComboBoxModel modelito = new DefaultComboBoxModel();
        jComboBox1.setModel(modelito);
        modelito.addElement("todos");
        ArrayList<Sector> sectores = Fachada.getInstancia().getsSector();
        for (Sector s : sectores) {
            modelito.addElement(s.getNombre());
        }
    }

    private String formatear(LLamada l) {
        return " Num: " + l.getNumLlamda() + " Estado "+l.estadostr + " Inicio: " + l.getFechaHoraIicio() + " Fin: " + l.getFechaHoraFin() + " Puesto: " + l.getPuesto()
                + " Trabajador: " + l.getTrabajador().nombre + " Duracion: " + l.calcularDuracionSegundos() + " Cliente: " + l.getCliente().nombre;
    }

    private String formatearTodo(LLamada l) {
        return l.trabajador.misector.nombre + " "+ " Num: " + l.getNumLlamda()+ "  Estado "+l.estadostr + " Inicio: " + l.getFechaHoraIicio() + " Fin: " + l.getFechaHoraFin() + " Puesto: " + l.getPuesto()
                + " Trabajador: " + l.getTrabajador().nombre + " Duracion: " + l.calcularDuracionSegundos() + " Cliente: " + l.getCliente().nombre;
    }

