    @Override
    public void mostrarLlamadas(ArrayList<LLamada> llamada) {
        ArrayList<String> list = new ArrayList();
        llamadas = llamada;
        for (LLamada l : llamadas) {
            if(l.trabajador!=null){
            list.add(formatear(l));}
        }
        listado.setListData(list.toArray());
    }

    @Override
    public void mostrarLlamadasTodos(ArrayList<LLamada> llamada) {
        ArrayList<String> list = new ArrayList();
        llamadas = llamada;
        for (LLamada l : llamadas) {
            if(l.trabajador!=null){
            list.add(formatearTodo(l));
            }
        }
        listado.setListData(list.toArray());

    }

